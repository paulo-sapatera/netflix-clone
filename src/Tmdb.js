// ela da um fetch na URL e pega o JSON de resultado e retorna varias vezes
// no fetch eu pego o a base e concateno com o endpoint
// pega o json também e retorna ele
// await é uma premissa que espera um valor, só pode ser usado em função async
const basicFetch = async (endpoint) => {
    const req  = await fetch(`${API_BASE}${endpoint}`);
    const json = await req.json();
    return json;
}

